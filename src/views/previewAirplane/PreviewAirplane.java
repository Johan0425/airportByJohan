    private void valideGroupSeats(int groupSeats) {
        if (groupSeats == 2) {
            loadSeatsByGroupSeats2();
        } else {
            loadSeatsByGroupSeats3();
        }
    }

    private void loadSeatsByGroupSeats2() {

        int width = 90;
        int height = 45;
        int separated = 40;
        int separated2 = 10;

        int text = 100;

        int x = 35;
        int y = 40;

        for (int i = 0; i < seats.length; i++) {
            for (int j = 0; j < seats[i].length; j++) {
                seats[i][j] = new JButton();
                seats[i][j].setBounds(x, y, width, height);

                seats[i][j].setBackground(Color.GRAY);
                seats[i][j].setText(String.valueOf("Puesto " + text));
                text++;

                seats[i][j].addActionListener(this);
                panelSeats.add(seats[i][j]);

                if (j == seats[i].length / 3 - 1 | j == seats[i].length / 1 - 3) {
                    x += width + 2 * separated;
                } else {
                    x += width + separated2;
                }
            }
            if (i == seats.length | i == seats.length) {
                y += height + 2 * separated;
            } else {
                y += height + separated2;
            }
            x = 35;

        }
    }

