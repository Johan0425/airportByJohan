    private void sendMessage() {

        User user = controller.getUser();

        if (user != null && user.getRole() == Role.AIRLINE_ADMIN) {
            String message = txtMessage.getText();
            txtAreaChat.append("Admin " + user.getFullname() + ":" + message + "\n");
            txtMessage.setText("");
        } else if (user != null && user.getRole() == Role.LOGISTICS_EMPLOYEE) {
            String message = txtMessage.getText();
            txtAreaChat.append("LogÃ­stica " + user.getFullname() + ":" + message + "\n");
            txtMessage.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "No tiene permisos para acceder al chat");
            this.dispose();
        }

    }

